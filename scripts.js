function add(a, b) {
    return (a + b);
}

function subtract(minuend, subtrahend) {
    return (minuend - subtrahend);
}

function multiply(a, b) {
    return (a * b);
}

function divide(dividend, divisor) {
    return (dividend / divisor);
}

function modulo(dividend, divisor) {
    return (dividend % divisor);
}